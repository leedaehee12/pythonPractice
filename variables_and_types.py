# variables_and_types.py

#정수형 변수
x = 10
print("x = ", x)
print("x의 타입", type(x))

#부동소수점변수
y= 3.14
print("y = ", y)
print("y의 타입은", type(y))

#문자열변수
name = "Jhon"
print("name = ", name)
print("name의 타입은", type(name))

#불리언변수
is_active = True
print("is_active = ", is_active)
print("is_active의 타입", type(is_active))