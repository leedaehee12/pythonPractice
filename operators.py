#operators.py

#산술연산자
a = 5
b = 2

print("a+b = ", a+b)
print("a-b = ", a-b)
print("a*b = ", a*b)
print("a/b = ", a/b)
print("a//b = ", a//b)
print("a % b= ", a%b)
print("a**b = ", a**b)

# 비교연산자
print("a==b = ", a == b)
print("a != b = ", a != b)
print("a > b = ", a > b)
print("a < b = " ,a < b)
print("a >= b = ", a >= b)
print("a <= b = ", a <= b)


#논리연산자
print("a > 0 and b > 0 = ", a > 0 and b > 0)
print("a > 0 or b > 0 = ", a > 0 or b > 0)
print("not(a > 0) = ", not(a > 0))